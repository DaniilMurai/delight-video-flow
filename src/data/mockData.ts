// Helper function to get video by id
export const getVideoById = (id: string) => {
  return videos.find(video => video.id === id);
};

// Helper function to get related videos
export const getRelatedVideos = (currentVideoId: string, limit: number = 4) => {
  const currentVideo = getVideoById(currentVideoId);
  if (!currentVideo) return [];
  
  // Get videos that share tags with the current video
  const relatedVideos = videos
    .filter(video => 
      video.id !== currentVideoId && 
      video.tags.some(tag => currentVideo.tags.includes(tag))
    )
    .slice(0, limit);
  
  return relatedVideos;
};

// Helper function to filter videos by tag
export const getVideosByTag = (tagSlug: string) => {
  return videos.filter(video => video.tags.includes(tagSlug));
};

// Helper function to filter videos by category
export const getVideosByCategory = (categorySlug: string) => {
  return videos.filter(video => video.categories.includes(categorySlug));
};
